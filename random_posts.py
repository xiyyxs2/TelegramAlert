def make_regular_post(simulation_label: bool = True) -> str:
    text = random.choice(REGULAR_POSTS + MEME_POSTS).format(region=random_region())
    return add_simulation_label(humanize_regular(text), simulation_label)


def make_blog_post(simulation_label: bool = True) -> str:
    text = random.choice(BLOG_POSTS).format(region=random_region())
    return add_simulation_label(humanize_regular(text), simulation_label)
