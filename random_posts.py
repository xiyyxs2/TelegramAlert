def _lang_kwargs() -> dict:
    return {key: random.choice(value) for key, value in LANG_BITS.items()}


def _fmt(text: str) -> str:
    return text.format(region=random_region(), threat=random_threat())


def make_regular_post(simulation_label: bool = True) -> str:
    pool = SHORT_STATUS_POSTS + CHANNEL_STYLE_POSTS + CALM_POSTS
    text = _fmt(random.choice(pool))
    if text in SHORT_STATUS_POSTS or len(text) < 45:
        text = ensure_dot(text)
    else:
        text = humanize_regular(text)
    return add_simulation_label(text, simulation_label)


def make_blog_post(simulation_label: bool = True) -> str:
    # 60% — ваш короткий стиль, 40% — блог/рандом на різних мовах.
    if random.random() < 0.40:
        template = random.choice(MULTILINGUAL_BLOG_POSTS)
        text = template.format(region=random_region(), threat=random_threat(), **_lang_kwargs())
    else:
        text = _fmt(random.choice(BLOG_STYLE_POSTS))
    return add_simulation_label(humanize_regular(text), simulation_label)


def make_repeat_post(simulation_label: bool = True) -> str | None:
    # Bot API не дає читати всю стару історію каналу, тому бот повторює:
    # 1) свої нові повідомлення з бази;
    # 2) стартові фрази у вашому стилі, поки база ще порожня.
    text = database.get_random_logged_text(types=("regular", "blog", "odd", "update", "weather", "wave"), limit=300)
    if not text:
        text = random.choice(SEEDED_OLD_STYLE)
    return add_simulation_label(text, simulation_label)
